 class calculator {
    constructor(){
        this.number = 0;
    }

    add(number1, number2){
        return this.number = number1 + number2
    }
    substract(number1, number2){
        return this.number = number1 - number2
    }
    multiply(number1, number2){
        return this.number = number1 * number2
    }
    divide(number1, number2){
        return this.number = number1 / number2
    }
 }

 module.exports =  calculator; 